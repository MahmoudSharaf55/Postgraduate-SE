package Model;

public class Stuff {
    String stuffName,stuffEmail,stuffphone,stuffPassword;
    String paperstate;


    public String getPaperstate() {
        return paperstate;
    }

    public void setPaperstate(String paperstate) {
        this.paperstate = paperstate;
    }

    public String getStuffName() {
        return stuffName;
    }

    public void setStuffName(String stuffName) {
        this.stuffName = stuffName;
    }



    public String getStuffEmail() {
        return stuffEmail;
    }

    public void setStuffEmail(String stuffEmail) {
        this.stuffEmail = stuffEmail;
    }

    public String getStuffphone() {
        return stuffphone;
    }

    public void setStuffphone(String stuffphone) {
        this.stuffphone = stuffphone;
    }

    public String getStuffPassword() {
        return stuffPassword;
    }

    public void setStuffPassword(String stuffPassword) {
        this.stuffPassword = stuffPassword;
    }
}
